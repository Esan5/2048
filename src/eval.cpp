#include "eval.hpp"

uint32_t eval::snakeEval(uint64_t board) {
  return ((board & 0xF000000000000000) >> 60) * 16 +
         ((board & 0x0F00000000000000) >> 56) * 15 +
         ((board & 0x00F0000000000000) >> 52) * 14 +
         ((board & 0x000F000000000000) >> 48) * 13 +
         ((board & 0x0000000F00000000) >> 44) * 12 +
         ((board & 0x000000F000000000) >> 40) * 11 +
         ((board & 0x00000F0000000000) >> 36) * 10 +
         ((board & 0x0000F00000000000) >> 32) *  9 +
         ((board & 0x00000000F0000000) >> 28) *  8 +
         ((board & 0x000000000F000000) >> 24) *  7 +
         ((board & 0x0000000000F00000) >> 20) *  6 +
         ((board & 0x00000000000F0000) >> 16) *  5 +
         ((board & 0x000000000000000F) >> 12) *  4 +
         ((board & 0x00000000000000F0) >> 8 ) *  3 +
         ((board & 0x0000000000000F00) >> 4 ) *  2 +
         ((board & 0x000000000000F000));
}

